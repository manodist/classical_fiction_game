/**
 * 작품의 음악 설정 가져오기
 * @param bookTitle 작품 제목
 * @returns 음악 설정 또는 null (비활성화/없음)
 */
export function getMusicConfig(bookTitle: string): MusicConfig | null {
  const config = BOOK_MUSIC[bookTitle];
  
  // 설정이 없거나 비활성화되어 있거나 URL이 없으면 null 반환
  if (!config || !config.enabled || !config.url) {
    return null;
  }
  
  return config;
}

/**
 * 모든 활성화된 작품의 음악 목록 가져오기
 * @returns [작품명, 설정] 쌍의 배열
 */
export function getAllActivatedMusic(): Array<[string, MusicConfig]> {
  return Object.entries(BOOK_MUSIC)
    .filter(([_, config]) => config.enabled && config.url);
}

/**
 * BGM이 설정된 작품 수 가져오기
 */
export function getMusicStats() {
  const total = Object.keys(BOOK_MUSIC).length;
  const activated = getAllActivatedMusic().length;
  const pending = total - activated;
  
  return {
    total,      // 전체 작품 수
    activated,  // BGM 활성화된 작품 수
    pending,    // BGM 대기 중인 작품 수
  };
}


